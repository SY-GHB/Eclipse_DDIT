package vo;

public class NoticeVo {
	private int noti_no;
	private int admin_no;
	private String noti_name;
	private String noti_content;
	private int noti_count;
	private String noti_date;
	private String noti_delyn;
	
	
	public int getNoti_no() {
		return noti_no;
	}
	public void setNoti_no(int noti_no) {
		this.noti_no = noti_no;
	}
	public int getAdmin_no() {
		return admin_no;
	}
	public void setAdmin_no(int admin_no) {
		this.admin_no = admin_no;
	}
	public String getNoti_name() {
		return noti_name;
	}
	public void setNoti_name(String noti_name) {
		this.noti_name = noti_name;
	}
	public String getNoti_content() {
		return noti_content;
	}
	public void setNoti_content(String noti_content) {
		this.noti_content = noti_content;
	}
	public int getNoti_count() {
		return noti_count;
	}
	public void setNoti_count(int noti_count) {
		this.noti_count = noti_count;
	}
	public String getNoti_date() {
		return noti_date;
	}
	public void setNoti_date(String noti_date) {
		this.noti_date = noti_date;
	}
	public String getNoti_delyn() {
		return noti_delyn;
	}
	public void setNoti_delyn(String noti_delyn) {
		this.noti_delyn = noti_delyn;
	}
	
	
	
}
