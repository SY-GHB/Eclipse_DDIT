package vo;

public class AttractionVo {
	private int att_no;
	private String att_name;
	private String att_content;
	private int att_person;
	private String att_location;
	private String att_spenttime;
	private String att_runyn;

	public int getAtt_no() {
		return att_no;
	}

	public void setAtt_no(int att_no) {
		this.att_no = att_no;
	}

	public String getAtt_name() {
		return att_name;
	}

	public void setAtt_name(String att_name) {
		this.att_name = att_name;
	}

	public String getAtt_content() {
		return att_content;
	}

	public void setAtt_content(String att_content) {
		this.att_content = att_content;
	}

	public int getAtt_person() {
		return att_person;
	}

	public void setAtt_person(int att_person) {
		this.att_person = att_person;
	}

	public String getAtt_location() {
		return att_location;
	}

	public void setAtt_location(String att_location) {
		this.att_location = att_location;
	}

	public String getAtt_spenttime() {
		return att_spenttime;
	}

	public void setAtt_spenttime(String att_spenttime) {
		this.att_spenttime = att_spenttime;
	}

	public String getAtt_runyn() {
		return att_runyn;
	}

	public void setAtt_runyn(String att_runyn) {
		this.att_runyn = att_runyn;
	}

}
